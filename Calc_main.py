# Создаём функции что-бы было удобнее пользоватся, а не вводить по многу раз a-b или a+b
def fold(a, b): # Вход 2 параметра 1число и второе
    print(f'Результат сложения: ', end="") # Выводим текст результата
    return a + b # Возвращаем результат

def ssub(a, b):
    print(f'Результат вычитания: ', end="")
    return a - b
